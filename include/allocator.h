#ifndef ALLOCATOR_H
#define ALLOCATOR_H

#include <stddef.h>  // для size_t

// Структура, представляющая сам аллокатор.
// Внутри она будет хранить служебные данные, специфичные для конкретного
// алгоритма. Мы используем void*, чтобы скрыть реализацию (инкапсуляция).
typedef struct Allocator Allocator;

// ============================================================
// ИНТЕРФЕЙС АЛЛОКАТОРА
// Эти функции должны быть реализованы для каждого алгоритма.
// ============================================================

// 1. Создание аллокатора
// realMemory - указатель на большой кусок памяти, который выделила ОС
// (например, через mmap или malloc). memory_size - размер этого куска. Функция
// должна инициализировать структуры данных внутри этой памяти.
Allocator* allocator_create(void* realMemory, size_t memory_size);

// 2. Выделение памяти
// allocator - указатель на наш аллокатор.
// size - сколько байт хочет пользователь.
// Возвращает указатель на выделенный блок или NULL, если места нет.
void* allocator_alloc(Allocator* allocator, size_t size);

// 3. Освобождение памяти
// allocator - указатель на наш аллокатор.
// block - указатель на блок, который нужно освободить.
void allocator_free(Allocator* allocator, void* block);

// 4. Уничтожение аллокатора (опционально, для очистки структур, если нужно)
// Но обычно мы просто "забываем" про память realMemory или делаем ей free()
// снаружи.
void allocator_destroy(Allocator* allocator);

#endif